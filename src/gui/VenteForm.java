    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
        try {
        int id = getSelectedId();
        if (id == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sélectionne une vente");
            return;
        }

        Vente v = getFormData(id);

        boolean ok = vs.modifier(v);

        if (ok) {
            javax.swing.JOptionPane.showMessageDialog(this, "✅ Vente modifiée avec succès");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "❌ Modification échouée");
        }

        loadAll();
        clear();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_modifierActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        // TODO add your handling code here:
        try {
        int id = getSelectedId();
        if (id == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Sélectionne une vente");
            return;
        }
        int rep = javax.swing.JOptionPane.showConfirmDialog(this, "Supprimer cette vente ?");
        if (rep == javax.swing.JOptionPane.YES_OPTION) {
            boolean ok = vs.supprimer(id);
            if (!ok) javax.swing.JOptionPane.showMessageDialog(this, "Suppression échouée");
            loadAll();
            clear();
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_supprimerActionPerformed

