    void loadFamilles() {
    famille.removeAllItems();
    famille.addItem("Toutes");

    Set<String> familles = new TreeSet<>();
    for (Medicament m : ms.listerTous()) {
        if (m.getFamille() != null && !m.getFamille().trim().isEmpty()) {
            familles.add(m.getFamille().trim());
        }
    }
    for (String f : familles) famille.addItem(f);
}

void loadMedicamentsAll() {
    Medicament.removeAllItems();
    for (Medicament m : ms.listerTous()) {
        Medicament.addItem(m); 
    }
}

void loadMedicamentsByFamille(String fam) {
    Medicament.removeAllItems();

    List<Medicament> list;
    if (cacheFamille.containsKey(fam)) list = cacheFamille.get(fam);
    else {
        list = ms.filtrerParFamille(fam);
        cacheFamille.put(fam, list);
    }

    for (Medicament m : list) Medicament.addItem(m);
}

void loadAll() {
    model.setRowCount(0);
    for (Vente v : vs.listerTous()) {
        model.addRow(new Object[]{
            v.getId(),
            v.getMedicamentId(),
            v.getDateVente(),
            v.getQuantite()
        });
    }
}

void loadByMedicament(int medicamentId) {
    model.setRowCount(0);
    for (Vente v : vs.findByMedicament(medicamentId)) {
        model.addRow(new Object[]{
            v.getId(),
            v.getMedicamentId(),
            v.getDateVente(),
            v.getQuantite()
        });
    }
}
private void selectMedicamentById(int medId) {
    for (int i = 0; i < Medicament.getItemCount(); i++) {
        Medicament m = (Medicament) Medicament.getItemAt(i);
        if (m.getId() == medId) {
            Medicament.setSelectedIndex(i);
            return;
        }
    }
}

void clear() {
    quantite.setText("");
    Date.setDate(null);
    jTable1.clearSelection();
}

int getSelectedId() {
    int row = jTable1.getSelectedRow();
    if (row < 0) return -1;
    return Integer.parseInt(model.getValueAt(row, 0).toString());
}

Vente getFormData(int id) {
    if (Medicament.getSelectedItem() == null)
        throw new IllegalArgumentException("Choisis un médicament");

    Medicament m = (Medicament) Medicament.getSelectedItem();

    java.util.Date d = Date.getDate();
    if (d == null) throw new IllegalArgumentException("Choisis une date");

    int q;
    try { q = Integer.parseInt(quantite.getText().trim()); }
    catch (Exception e) { throw new IllegalArgumentException("Quantité invalide"); }

    if (q <= 0) throw new IllegalArgumentException("Quantité doit être > 0");

    return new Vente(id, m.getId(), new java.sql.Date(d.getTime()), q);
}


