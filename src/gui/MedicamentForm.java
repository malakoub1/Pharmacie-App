    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
         if (selectedId == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Sélectionne une ligne d'abord !");
        return;
    }

    try {
    Medicament m = new Medicament();
    m.setId(selectedId);
    m.setNom(nom.getText().trim());
    m.setFamille(famille.getText().trim());
    m.setPrix(Double.parseDouble(prix.getText().trim()));
    m.setStock(Integer.parseInt(stock.getText().trim()));
    m.setSeuilAlerte(Integer.parseInt(seuil.getText().trim()));

    Fournisseur selected = (Fournisseur) fournisseur.getSelectedItem();
    Integer fid = (selected == null || selected.getId() == 0) ? null : selected.getId();
    m.setFournisseurId(fid);

    boolean ok = ms.modifier(m);

    if (ok) {
        javax.swing.JOptionPane.showMessageDialog(this, "✅ Modifié avec succès");
        load();
        clear();
        selectedId = -1;
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "❌ Modification échouée");
    }

} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
}

    }//GEN-LAST:event_modifierActionPerformed

