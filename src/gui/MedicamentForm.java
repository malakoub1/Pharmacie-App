    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
            int row = jTable1.getSelectedRow();
    if (row == -1) return;

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    // 0 = colonne ID (la 1ère colonne)
    selectedId = Integer.parseInt(model.getValueAt(row, 0).toString());

    
    nom.setText(model.getValueAt(row, 1).toString());
    famille.setText(model.getValueAt(row, 2).toString());
    prix.setText(model.getValueAt(row, 3).toString());
    stock.setText(model.getValueAt(row, 4).toString());
    seuil.setText(model.getValueAt(row, 5).toString());
    // FournisseurID est colonne 6
Object fidObj = model.getValueAt(row, 6);
Integer fid = (fidObj == null) ? null : Integer.valueOf(fidObj.toString());

if (fid == null || fid == 0) {
    fournisseur.setSelectedIndex(0); 
} else {
    boolean found = false;
    for (int i = 0; i < fournisseur.getItemCount(); i++) {
        Fournisseur fo = (Fournisseur) fournisseur.getItemAt(i);
        if (fo != null && fo.getId() == fid) {
            fournisseur.setSelectedIndex(i);
            found = true;
            break;
        }
    }
    if (!found) fournisseur.setSelectedIndex(0);
}
    }//GEN-LAST:event_jTable1MouseClicked


