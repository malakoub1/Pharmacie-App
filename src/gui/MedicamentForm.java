    void loadFournisseurs() {
    fournisseur.removeAllItems();
    fournisseur.addItem(new Fournisseur(0, "(Aucun)", "", ""));

    for (Fournisseur f : fs.listerTous()) {
        fournisseur.addItem(f);
    }
}

void load() {
    model.setRowCount(0);
    for (Medicament m : ms.listerTous()) {
        model.addRow(new Object[]{
            m.getId(),
            m.getNom(),
            m.getFamille(),
            m.getPrix(),
            m.getStock(),
            m.getSeuilAlerte(),
            m.getFournisseurId()
        });
    }
}

void clear() {
    nom.setText("");
    famille.setText("");
    prix.setText("");
    stock.setText("");
    seuil.setText("");
    fournisseur.setSelectedIndex(0);
    jTable1.clearSelection();
}

Medicament getFormData(int id) {
    String n = nom.getText().trim();
    String f = famille.getText().trim();

    if (n.isEmpty() || f.isEmpty())
        throw new IllegalArgumentException("Nom et Famille obligatoires");

    double p;
    int st;
    int se;

    try { p = Double.parseDouble(prix.getText().trim()); }
    catch (Exception e) { throw new IllegalArgumentException("Prix invalide"); }

    try { st = Integer.parseInt(stock.getText().trim()); }
    catch (Exception e) { throw new IllegalArgumentException("Stock invalide"); }

    try { se = Integer.parseInt(seuil.getText().trim()); }
    catch (Exception e) { throw new IllegalArgumentException("Seuil invalide"); }

    if (p < 0) throw new IllegalArgumentException("Prix doit être >= 0");
    if (st < 0) throw new IllegalArgumentException("Stock doit être >= 0");
    if (se < 0) throw new IllegalArgumentException("Seuil doit être >= 0");

    Fournisseur selected = (Fournisseur) fournisseur.getSelectedItem();
    Integer fid = (selected == null || selected.getId() == 0) ? null : selected.getId();

    return new Medicament(id, n, f, p, st, se, fid);
}

int getSelectedId() {
    int row = jTable1.getSelectedRow();
    if (row < 0) return -1;
    return Integer.parseInt(model.getValueAt(row, 0).toString());
}

